def remove_html(text):
    return re.sub(r'<[^>]*>', '', text)

def removeIcon(text):
    pattern = r"[^a-zA-^\p{L}\p{N}\s\.,!?;:_/\\-]"  
    return re.sub(pattern, '', text).strip()

